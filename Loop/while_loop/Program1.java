// print number from 1 to 10
public class Program1 {
    public static void main(String[] args) {
        System.out.println("main starts");
        int i=1;
        while(i<=10)
        {
            System.out.print(i+" ");
            i++;
        }
        System.out.println("\n");
        System.out.println("Main ends");
        
    }
}
